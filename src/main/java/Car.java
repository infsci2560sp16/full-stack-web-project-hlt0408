public class Car {
	int id;
	String make;
	String model;
	int year;
	String bodytype;
	int price;
	String path;
	public int getid() {
		return id;
	}
	public String getmake() {
		return make;
	}
	public String getmodel() {
		return model;
	}
	public int getyear() {
		return year;
	}
	public String getbodytype() {
		return bodytype;
	}
	public int getprice() {
		return price;
	}
	public String getpath() {
		return path;
	}
}