public class Car {
	String make;
	String model;
	int year;
	String bodytype;
	String price;
	
	public String getmake() {
		return make;
	}
	public String getmodel() {
		return model;
	}
	public int getyear() {
		return year;
	}
	public String getbodytype() {
		return bodytype;
	}
	public String getprice() {
		return price;
	}
}